    private void txtdatoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtdatoKeyReleased
         jl_validacion.setIcon(null);
        String cadena = txtdato.getText();
        if (cadena.matches("^[a-zA-Z]{1,15}$")){//verifica que solo tenga letras, y que esté entre 1 y 15
            
            jl_validacion.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/cheque-32px.png")));

        }else if(cadena.matches(".*[0-9].*")){//verifica que no hayan numeros ni otros simbolos en la cadena
           
            jl_validacion.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/cancelar-32px.png")));
        
        }else{
           
            jl_validacion.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/cancelar-32px.png")));
            
        }
    }//GEN-LAST:event_txtdatoKeyReleased

    private void numcedulaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_numcedulaKeyReleased
        jl_validacion2.setIcon(null);
        String cadena2 = numcedula.getText();
        if(cadena2.matches("^[0-9]{10}$")){//verifica que tenga 10 dígitos
            
            jl_validacion2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/cheque-32px.png")));
        
        }else if (cadena2.matches(".*[a-zA-Z].*")){//verifica que no contenga letras ni simbolos
        
            jl_validacion2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/cancelar-32px.png")));
        
        }
        else{
            
            jl_validacion2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/cancelar-32px.png")));
            
        }     
    }//GEN-LAST:event_numcedulaKeyReleased


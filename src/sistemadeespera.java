    private void botonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonMouseClicked
        codigo ven1 = new codigo();
        ven1.setDato("Bienvenido "+txtdato.getText());
        
        if(numcedula.getText().equals("") || txtdato.getText().equals("")){
            jl_ndcMensaje.setForeground(Color.red);
            jl_userMensaje.setForeground(Color.red);
            JOptionPane.showMessageDialog(null, "FALTAN DATOS!","ERROR",JOptionPane.ERROR_MESSAGE);
        }else if(!numcedula.getText().matches("\\d+") || numcedula.getText().length()!=10 || txtdato.getText().matches(".*\\d.*")){
            numcedula.setText("");
            txtdato.setText("");
            jl_ndcMensaje.setForeground(Color.red);
            jl_userMensaje.setForeground(Color.red);
            JOptionPane.showMessageDialog(null, "Usuario o número de cédula incorrectos","ERROR",JOptionPane.ERROR_MESSAGE);
        }else{
            ven1.setVisible(true);
            this.dispose();
        }      
    }//GEN-LAST:event_botonMouseClicked

